package model.professor;

/**
 *
 * @author smatcha
 */
public class Professor {
    private String ProfessorName;
    private String UniversityThought;
    private String CourseThought;
    private float Experience;

    public String getProfessorName() {
        return ProfessorName;
    }

    public void setProfessorName(String ProfessorName) {
        this.ProfessorName = ProfessorName;
    }

    public String getUniversityThought() {
        return UniversityThought;
    }

    public void setUniversityThought(String UniversityThought) {
        this.UniversityThought = UniversityThought;
    }

    public String getCourseThought() {
        return CourseThought;
    }

    public void setCourseThought(String CourseThought) {
        this.CourseThought = CourseThought;
    }

    public float getExperience() {
        return Experience;
    }

    public void setExperience(float Experience) {
        this.Experience = Experience;
    }
    
    
    
    
}
